$(document).ready(function(){
    $('.openbutton').click(function(event){
        event.preventDefault()
        $('.info_text').toggleClass('infotexthide');
        $('.infotexthidden').toggleClass('infotextnothidden')
        $('.openbutton').toggleClass('openbuttonhidden')
        //$('.openbuttonhidden').toggleClass('openbutton')
    });
});
